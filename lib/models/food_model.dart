class Food {
  String description;
  double kcal;
  double protein;
  double carbohydrates;
  double fat;

  Food(String description, double kcal, double protein, double carbohydrates,
      double fat) {
    this.description = description;
    this.kcal = kcal;
    this.protein = protein;
    this.carbohydrates = carbohydrates;
    this.fat = fat;
  }
  
}
